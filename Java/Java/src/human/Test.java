package human;

// 폰트크기 조절 	:  ctrl + (+/-) (백스페이스 옆, 숫자키패드 말고)
// 전체화면		:  ctrl + M
/**
 * 자바 프로그램 첫번째 예제
 * @author h
 * 
 */           
public class Test {	
	// 클래스 선언 : public class 클래스명 { }
	// * 클래스 이름은 클래스 파일 이름과 일치해야한다.
	
	// 클래스 안에는 변수와 메소드를 정의
	
	// main() : 프로그램 실행 시, 가장 먼저 실행되는 메소드
	// main : ctrl + space
	/**
	 * 프로그램의 시작이 되는 메소드
	 * @param args
	 */
	public static void main(String[] args) {
		// 기본 출력문
		// sysout : ctrl + space
		System.out.println("Hello Java~!");	
		// 실행문 : 코드 한줄마다 ;(세미콜론)으로 완성  
	}
	
	// 주석 : 프로그램 실행에 영향을 미치지 않는 코드설명 또는 메모
	//  - 기호 
	//    * 한 줄 주석 	: // 내용			
	//					ctrl + /  또는  clrl + shift + C
	//    * 여러 줄 주석 	: /* 내용 */
	//					주석 : ctrl + shift + /
	//					해제 : ctrl + shift + \ (역슬래시)
	//    * 문서 정의 주석 : /** 문서 내용 */
	//					클래스 위에, 메소드 위에 정보를 나타날 때 사용
	
	// 코드 수정 시, 윈도우 탭의 파일명 앞에 * 기호가 붙는다.
	// 		       현재 코드가 수정상태라는 것을 알려준다.
	// ex) *Test.java  
	// 코드 작성/수정 후, 파일을 저장해주세요. - ctrl + S
	
	// 프로그램 실행 : [Run]메뉴 > [Run]
	//			    - ctrl + F11
	// 프로그램 실행 시, 출력 및 에러 결과가 Console 탭에 나온다.

}
